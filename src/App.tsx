
import { Toaster } from "@/components/ui/toaster";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Routes, Route } from "react-router-dom";
import MainLayout from "./components/layout/MainLayout";
import Dashboard from "./pages/Dashboard";
import MembersDirectory from "./pages/MembersDirectory";
import EventsManagement from "./pages/EventsManagement";
import Announcements from "./pages/Announcements";
import Budget from "./pages/Budget";
import NotFound from "./pages/NotFound";

const queryClient = new QueryClient();

const App = () => (
  <QueryClientProvider client={queryClient}>
    <TooltipProvider>
      <Toaster />
      <Sonner />
      <BrowserRouter>
        <Routes>
          <Route path="/" element={<MainLayout><Dashboard /></MainLayout>} />
          <Route path="/members" element={<MainLayout><MembersDirectory /></MainLayout>} />
          <Route path="/events" element={<MainLayout><EventsManagement /></MainLayout>} />
          <Route path="/announcements" element={<MainLayout><Announcements /></MainLayout>} />
          <Route path="/budget" element={<MainLayout><Budget /></MainLayout>} />
          <Route path="*" element={<NotFound />} />
        </Routes>
      </BrowserRouter>
    </TooltipProvider>
  </QueryClientProvider>
);

export default App;
